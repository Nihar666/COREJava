package com.pkg.oops;

public class BitwiseOperator {
	public static void main(String[] args) {
		int x=10;
		int y=11;
		System.out.println(x&y);
		System.out.println("=========");
		System.out.println(x|y);
		System.out.println("===========");
		System.out.println(~x);
		System.out.println("=========");
		System.out.println(x^y);
		System.out.println("=========");
		System.out.println(x<<2);
		System.out.println("===========");
		System.out.println(x>>2);
		System.out.println("===========");
		System.out.println(x>>>2);
		
		
		
	}

}
